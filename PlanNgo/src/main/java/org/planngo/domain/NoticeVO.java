package org.planngo.domain;

import java.util.Date;

import lombok.AllArgsConstructor;
import lombok.Data;
import lombok.NoArgsConstructor;

@Data

@NoArgsConstructor
@AllArgsConstructor

public class NoticeVO {
	
	private Integer NOTICECODE;
	private Integer USRCODE;
	private String USRNAME;
	private String NOTICETITLE;
	private String NOTICECONTENT;
	private Date REGDATE;
	
	@Override
	public String toString() {
		return "BoardVO [NOTICECODE=" + NOTICECODE + ", NOTICETITLE=" +  NOTICETITLE + ", USRNAME=" + USRNAME + ", NOTICECONTENT="
				+ NOTICECONTENT + ", REGDATE=" + REGDATE + ", USRCODE=" + USRCODE + "]";
	}
}
